#!/usr/bin/env node
const fs = require('node:fs');
const path = require('node:path');
const { spawnSync } = require('node:child_process');

const root = path.resolve(__dirname, '..', '..');
const reportPath = path.join(root, 'docs/audit/BASE_CONTRACT_GATE_MATURITY_REPORT.md');

const gates = [
  { name: 'ACCEPTANCE_OPERATION_STATE_TRUST_GATE_V1', maturity: 'ci_enforced', command: 'node', args: ['scripts/governance_acceptance/ACCEPTANCE_OPERATION_STATE_TRUST_GATE_V1.cjs'], contract: 'OperationState Trust Gate' },
  { name: 'ACCEPTANCE_FORMAL_ACCEPTANCE_GATE_V1', maturity: 'ci_enforced', command: 'node', args: ['scripts/governance_acceptance/ACCEPTANCE_FORMAL_ACCEPTANCE_GATE_V1.cjs'], contract: 'Acceptance Formal Gate' },
  { name: 'ACCEPTANCE_FLIGHT_TABLE_NOT_FORMAL_EVIDENCE_V1', maturity: 'ci_enforced', command: 'node', args: ['scripts/governance_acceptance/ACCEPTANCE_FLIGHT_TABLE_NOT_FORMAL_EVIDENCE_V1.cjs'], contract: 'Evidence Artifact / Flight Table Boundary' },
  { name: 'ACCEPTANCE_OPERATION_REPORT_GUARDED_PAYLOAD_V1', maturity: 'ci_enforced', command: 'node', args: ['scripts/governance_acceptance/ACCEPTANCE_OPERATION_REPORT_GUARDED_PAYLOAD_V1.cjs'], contract: 'Operation Report Chain Guard' },
  { name: 'ACCEPTANCE_CUSTOMER_DASHBOARD_PROJECTION_TRUST_V1', maturity: 'ci_enforced', command: 'node', args: ['scripts/governance_acceptance/ACCEPTANCE_CUSTOMER_DASHBOARD_PROJECTION_TRUST_V1.cjs'], contract: 'Dashboard / FieldReport Projection Trust' },
  { name: 'ACCEPTANCE_ROI_MEMORY_TRUST_LANE_V1', maturity: 'ci_enforced', command: 'node', args: ['scripts/governance_acceptance/ACCEPTANCE_ROI_MEMORY_TRUST_LANE_V1.cjs'], contract: 'ROI / Field Memory Trust Lane' },
  { name: 'ACCEPTANCE_FRONTEND_CUSTOMER_TRUST_GATE_V1', maturity: 'ci_enforced', command: 'node', args: ['scripts/governance_acceptance/ACCEPTANCE_FRONTEND_CUSTOMER_TRUST_GATE_V1.cjs'], contract: 'Frontend Customer Trust Gate' },
  { name: 'ACCEPTANCE_DEVTOOLS_SIMULATOR_NOT_FORMAL_V1', maturity: 'ci_enforced', command: 'node', args: ['scripts/governance_acceptance/ACCEPTANCE_DEVTOOLS_SIMULATOR_NOT_FORMAL_V1.cjs'], contract: 'Devtools / Simulator Boundary' },
  { name: 'ACCEPTANCE_AO_ACT_AO_SENSE_BOUNDARY_V1', maturity: 'ci_enforced', command: 'node', args: ['scripts/governance_acceptance/ACCEPTANCE_AO_ACT_AO_SENSE_BOUNDARY_V1.cjs'], contract: 'AO-ACT / AO-SENSE Boundary' },
  { name: 'ACCEPTANCE_AO_ACT_VERSION_BOUNDARY_V1', maturity: 'ci_enforced', command: 'node', args: ['scripts/governance_acceptance/ACCEPTANCE_AO_ACT_VERSION_BOUNDARY_V1.cjs'], contract: 'AO-ACT API / Fact Version Boundary' },
  { name: 'PDI_RELEASE_GATE', maturity: 'ci_enforced', command: 'node', args: ['scripts/agronomy_acceptance/ACCEPTANCE_PEST_DISEASE_INSPECTION_RELEASE_GATE.cjs'], contract: 'PDI Customer Evidence Basis / Scenario Release Gate' },
  { name: 'SERVER_TYPECHECK', maturity: 'ci_enforced', command: 'pnpm', args: ['--filter', '@geox/server', 'typecheck'], contract: 'Server Compile Contract' },
  { name: 'WEB_TYPECHECK', maturity: 'ci_enforced', command: 'pnpm', args: ['--filter', '@geox/web', 'typecheck'], contract: 'Web Compile Contract' },
];

function ensureCommandTargetExists(gate) {
  if (gate.command !== 'node') return;
  const scriptPath = gate.args.find((arg) => arg.endsWith('.cjs') || arg.endsWith('.mjs'));
  if (!scriptPath) return;
  const abs = path.join(root, scriptPath);
  if (!fs.existsSync(abs)) throw new Error(`missing required gate script for ${gate.name}: ${scriptPath}`);
}

function runGate(gate) {
  ensureCommandTargetExists(gate);
  const startedAt = new Date();
  const startedMs = Date.now();
  console.log(`\n[base-contract-p0-release-gate] START ${gate.name}`);
  console.log(`[base-contract-p0-release-gate] $ ${gate.command} ${gate.args.join(' ')}`);
  const result = spawnSync(gate.command, gate.args, { cwd: root, env: process.env, stdio: 'inherit', shell: process.platform === 'win32' });
  const elapsedMs = Date.now() - startedMs;
  if (result.error) return { ...gate, status: 'ERROR', exit_code: null, elapsed_ms: elapsedMs, started_at: startedAt.toISOString(), error: String(result.error?.message ?? result.error) };
  if (result.status !== 0) return { ...gate, status: 'FAIL', exit_code: result.status ?? 1, elapsed_ms: elapsedMs, started_at: startedAt.toISOString(), error: null };
  return { ...gate, status: 'PASS', exit_code: 0, elapsed_ms: elapsedMs, started_at: startedAt.toISOString(), error: null };
}

function writeReport(results, overallStatus) {
  const generatedAt = new Date().toISOString();
  const rows = results.map((r) => `| ${r.name} | ${r.contract} | ${r.maturity} | ${r.status} | ${r.exit_code ?? ''} | ${r.elapsed_ms} |`).join('\n');
  const failed = results.filter((r) => r.status !== 'PASS');
  const content = `# Base Contract P0 Gate Maturity Report\n\nStatus: ${overallStatus}\nGenerated by: scripts/governance_acceptance/ACCEPTANCE_BASE_CONTRACT_P0_RELEASE_GATE_V1.cjs\nGenerated at: ${generatedAt}\n\n## Scope\n\nThis report records the CI-enforced P0 negative gates for Base Contract v2. It upgrades the listed gates from \`script_exists\` / \`aggregated_gate_exists\` to \`ci_enforced\` only when the aggregate command completes successfully in GitHub Actions and local execution.\n\nThis report does not claim complete business correctness, full commercial readiness, or full runtime error-envelope migration. It only records the P0 negative-gate enforcement boundary.\n\n## Aggregate command\n\n\`pnpm run ci:base-contract:p0\`\n\n## Gate results\n\n| Gate | Contract area | gate_maturity | Status | Exit code | Elapsed ms |\n| --- | --- | --- | --- | --- | ---: |\n${rows}\n\n## Failed gates\n\n${failed.length === 0 ? '- None' : failed.map((r) => `- ${r.name}: ${r.error ?? `exit ${r.exit_code}`}`).join('\n')}\n\n## CI requirement\n\nThe GitHub Actions PR workflow must run \`pnpm run ci:base-contract:p0\`. If this aggregate gate is removed from CI, the gate maturity must be downgraded from \`ci_enforced\`.\n`;
  fs.mkdirSync(path.dirname(reportPath), { recursive: true });
  fs.writeFileSync(reportPath, content, 'utf8');
}

(function main() {
  console.log('[base-contract-p0-release-gate] running Base Contract P0 aggregate negative gate');
  const runtimeRequired = ["BASE_URL", "API_BASE_URL", "DATABASE_URL", "ADMIN_TOKEN", "AO_ACT_TOKEN"];
  const missingRuntime = runtimeRequired.filter((key) => !String(process.env[key] ?? "").trim());
  if (missingRuntime.length > 0) {
    console.error(`[base-contract-p0-release-gate] missing runtime env: ${missingRuntime.join(", ")}`);
    process.exit(1);
  }
  console.log('[base-contract-p0-release-gate] environment', {
    BASE_URL: process.env.BASE_URL ?? null,
    API_BASE_URL: process.env.API_BASE_URL ?? null,
    DATABASE_URL: process.env.DATABASE_URL ? '<set>' : '<missing>',
    AO_ACT_TOKEN: process.env.AO_ACT_TOKEN ? '<set>' : '<missing>',
    ADMIN_TOKEN: process.env.ADMIN_TOKEN ? '<set>' : '<missing>',
  });
  const results = [];
  for (const gate of gates) {
    let result;
    try { result = runGate(gate); }
    catch (err) { result = { ...gate, status: 'ERROR', exit_code: null, elapsed_ms: 0, started_at: new Date().toISOString(), error: String(err?.message ?? err) }; }
    results.push(result);
    if (result.status !== 'PASS') {
      console.error(`[base-contract-p0-release-gate] ${result.status} ${gate.name}`, result.error ?? `exit ${result.exit_code}`);
      writeReport(results, 'FAIL');
      process.exit(result.exit_code ?? 1);
    }
    console.log(`[base-contract-p0-release-gate] PASS ${gate.name}`, { elapsed_ms: result.elapsed_ms });
  }
  writeReport(results, 'PASS');
  console.log('\n[base-contract-p0-release-gate] PASS Base Contract P0 aggregate negative gate', { gates: results.length, report: path.relative(root, reportPath) });
})();